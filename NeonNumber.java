public class NeonNumber{
    public static void main(String[] args){
        int n=34;
        int sq=n*n;
        int sum=0;
        for(; sq!=0; sq/=10){
            int d=sq%10;
            sum+=d;
        }
        if(sum==n)
        System.out.println("Neon number");
        else
        System.out.println("Not neon number");
    }
}

